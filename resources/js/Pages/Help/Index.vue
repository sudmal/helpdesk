      <HelpSection title="Расписание бригад" icon="📅">
        <p>Раздел <b>Бригады → кнопка «Расписание»</b> на карточке бригады.</p>
        <p>Расписание формируется помесячно. Для каждого члена бригады на каждый день месяца задаётся статус: <b>Рабочий день</b>, <b>Выходной</b> или <b>Пожелание</b> (предпочтение сотрудника перед генерацией).</p>

        <p><b>Сетка расписания</b></p>
        <ul>
          <li>Строки — сотрудники бригады, столбцы — дни месяца.</li>
          <li>Строка <b>«На участке»</b> внизу — количество работающих сотрудников в каждый день. Ячейка подсвечивается красным, если число работников ниже минимума (2 чел.).</li>
          <li>Столбец <b>«Выходов»</b> справа — количество рабочих выездов каждого сотрудника за месяц. Зелёный цвет — достигнут целевой показатель; оранжевый — ниже цели.</li>
        </ul>

        <p><b>Цветовая маркировка ячеек</b></p>
        <ul>
          <li><b>Зелёный</b> — рабочий день (будни).</li>
          <li><b>Розовый</b> — рабочий день (суббота или воскресенье; бригады работают 7 дней в неделю).</li>
          <li><b>Серый</b> — выходной день.</li>
          <li><b>Жёлтый</b> — пожелание: сотрудник хотел бы взять выходной в этот день. Учитывается при генерации, но не гарантируется, если нарушает ограничения.</li>
          <li><b>Фиолетовый</b> — праздничный день (глобальный, для всех бригад). Все сотрудники отмечаются как «выходной», в строке «На участке» отображается «—».</li>
        </ul>

        <p><b>Режимы работы (переключатель вверху)</b></p>
        <ul>
          <li><b>«Отметить пожелания»</b> — клик по ячейке переключает её между «рабочий» и «пожелание». Используется до генерации расписания: сотрудники (или администратор за них) фиксируют предпочтительные дни отдыха.</li>
          <li><b>«Редактировать итог»</b> — клик по ячейке переключает между «рабочий» и «выходной». Используется после генерации для ручной корректировки результата.</li>
        </ul>

        <p><b>Праздничные дни</b></p>
        <ul>
          <li>Клик по числу дня в заголовке таблицы отмечает этот день праздником глобально (для всех бригад). При создании предлагается ввести название праздника (необязательно).</li>
          <li>Повторный клик по праздничному дню снимает его статус.</li>
          <li>Праздники хранятся в базе данных и сохраняются между сеансами. Изменение праздника немедленно отражается на сетке без перезагрузки страницы.</li>
        </ul>

        <p><b>Поле «Выходов макс.»</b></p>
        <ul>
          <li>Задаёт целевое количество рабочих выездов для каждого сотрудника в месяце. По умолчанию — 24.</li>
          <li>Алгоритм генерации стремится назначить каждому не более указанного числа рабочих дней. Значение можно уменьшить для более коротких месяцев или при плановых отпусках.</li>
        </ul>

        <p><b>Автоматическая генерация</b></p>
        <ul>
          <li>Нажмите <b>«Сгенерировать»</b>. Алгоритм работает в два этапа:
            <ul>
              <li><b>Шаг 1 — пожелания.</b> Помеченные пожелания обрабатываются по принципу «наименее оспариваемый день первым»: сначала дни, на которые пожелание подали меньше всего сотрудников. Пожелание выполняется, только если после его учёта в этот день останется не менее 2 работников. Иначе — игнорируется, сотрудник остаётся на рабочем дне.</li>
              <li><b>Шаг 2 — равномерное распределение.</b> Для каждого сотрудника вычисляется, сколько дополнительных выходных необходимо для достижения целевого показателя (дней в месяце − «Выходов макс.»). Выходные добавляются по одному: каждый раз выбирается день с максимальным расстоянием от уже назначенных выходных — это обеспечивает равномерный разброс по всему месяцу. Жёсткое ограничение: <b>не более 2 выходных дней подряд</b> (3 и более последовательных выходных не допускаются). Минимум 2 работника на участке соблюдается.</li>
            </ul>
          </li>
          <li>После генерации режим автоматически переключается на <b>«Редактировать итог»</b> для ручной корректировки.</li>
          <li>Генерацию можно запускать повторно: текущие пожелания будут переданы заново, итоговые выходные пересчитаются.</li>
        </ul>

        <p><b>Сохранение</b></p>
        <ul>
          <li>Кнопка <b>«Сохранить расписание»</b> записывает текущее состояние сетки в базу данных. Данные сохраняются постоянно и загружаются при следующем открытии страницы.</li>
          <li>Расписание хранится отдельно для каждой бригады и каждого месяца.</li>
          <li>После успешного сохранения кратковременно отображается метка «✓ Сохранено».</li>
        </ul>

        <p><b>Печать расписания</b></p>
        <ul>
          <li>Кнопка <b>«Печать»</b> вызывает диалог печати браузера, оптимизированный для Ч/Б принтера.</li>
          <li>При печати: панель управления и легенда скрываются; цветовая заливка заменяется оттенками серого (выходной — тёмный, праздник — средний, рабочий — белый).</li>
          <li>Ориентация страницы автоматически переключается на горизонтальную (A4 landscape) для размещения всех столбцов месяца.</li>
        </ul>

        <p><b>Навигация по месяцам</b></p>
        <ul>
          <li>Стрелки <b>‹</b> и <b>›</b> рядом с названием месяца переключают отображаемый месяц. При переходе страница перезагружается и загружает сохранённое расписание выбранного месяца.</li>
        </ul>

        <p><b>Предупреждения о нарушениях</b></p>
        <ul>
          <li>Если после редактирования остались рабочие дни с числом работников ниже минимума (2 чел.), внизу страницы отображается предупреждение. Нарушающие ячейки подсвечены красным в строке «На участке».</li>
        </ul>
      </HelpSection>

      <HelpSection title="Аналитика и отчёты" icon="📈">
        <p>Раздел <b>«Отчёты»</b> в левом меню. Доступен администраторам и руководителям поддержки.</p>
        <p>Для каждого отчёта задаётся произвольный <b>период анализа</b> (поля «с» / «по»). Данные обновляются при изменении периода.</p>
        <p><b>Доступные отчёты (вкладки):</b></p>
        <ul>
          <li><b>Нагрузка на бригады</b> — горизонтальная гистограмма: количество заявок на каждую бригаду за период. Помогает оценить загруженность команд и выявить дисбаланс. Таблица: бригада, количество заявок, доля.</li>
          <li><b>Частота по территориям</b> — горизонтальная гистограмма: количество заявок по каждой территории. Помогает выявить проблемные участки. Таблица с территориями и долями.</li>
          <li><b>Динамика материалов</b> — линейный график: суммарный расход материалов (в рублях) по месяцам внутри выбранного периода. Позволяет отслеживать сезонность и тренды. Таблица с разбивкой по месяцам: количество позиций и сумма.</li>
          <li><b>Соблюдение сроков</b> — столбчатая диаграмма с накоплением: для каждой бригады — доля заявок, закрытых вовремя, и доля просроченных. Сводка вверху — итоговые цифры по всем бригадам. Таблица: бригада, выполнено в срок, просрочено, всего.</li>
          <li><b>Распределение по дням</b> — многолинейный график: количество заявок каждого типа (подключение, ремонт, восстановление и т. д.) по дням месяца или дням недели. Переключатель «По дням месяца / По дням недели» меняет ось X. Разные типы заявок отображаются разными цветами. Под графиком — таблица-легенда с итогами по каждому типу за период.</li>
        </ul>
      </HelpSection>

      <HelpSection title="Территории" icon="🗺️">
