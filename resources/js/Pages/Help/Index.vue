      <HelpSection title="Журнал звонков" icon="📞">
        <p>Вкладка <b>«Журнал звонков»</b> в разделе «Звонки» — доступна сотрудникам с правом «Просмотр звонков» (по умолчанию не у всех ролей, настраивается в Настройки → Роли).</p>
        <p><b>По умолчанию показываются звонки за сегодня.</b> Кнопки периода позволяют переключиться на Неделю или Месяц.</p>
        <p><b>Статусы звонков (из очереди АТС):</b></p>
        <ul>
          <li><b>Принят</b> (зелёный) — звонок был в очереди и оператор ответил на него.</li>
          <li><b>Пропущен</b> (красный) — звонок был в очереди, но абонент отключился до ответа.</li>
          <li><b>Не в очереди</b> (серый) — звонок зафиксирован АТС, но не проходил через очередь (например, прямой звонок на внутренний номер). Статус отсутствует.</li>
        </ul>
        <p><b>Блок статистики:</b></p>
        <ul>
          <li>Показывает итоги за выбранный период: всего звонков, принято, пропущено, не в очереди.</li>
          <li>Рядом — небольшая круговая диаграмма соотношения принятых / пропущенных.</li>
          <li>Статистика пересчитывается с учётом всех активных фильтров (телефон, адрес, статус и т. д.).</li>
        </ul>
        <p><b>Фильтры:</b></p>
        <ul>
          <li><b>Телефон</b> — поиск по последним 7 цифрам номера.</li>
          <li><b>Адрес</b> — частичное совпадение с адресом из биллинга.</li>
          <li><b>Дата от / до</b> — произвольный диапазон.</li>
          <li><b>Статус очереди</b> — Принят / Пропущен / Не в очереди.</li>
          <li><b>Сопоставлен</b> — показывать только звонки с найденным или не найденным адресом в базе.</li>
        </ul>
        <p><b>Таблица:</b> дата и время, статус звонка, телефон, адрес из биллинга, квартира, сопоставленный адрес, время ожидания в очереди, оператор (внутренний номер). Адрес из базы отображается зелёной меткой — ссылка «заявки →» открывает все заявки по нему.</p>
