        <p><b>Строка итогов вверху:</b> ожидают в очереди / разговаривают / активных операторов / всего операторов.</p>
        <p><b>В очереди:</b> список абонентов, ожидающих ответа. Для каждого — порядковый номер в очереди, телефон, адрес из биллинга и время ожидания. Время ожидания подсвечивается оранжевым при превышении 2 минут и красным при 5+ минутах.</p>
        <p><b>Операторы:</b> список участников очереди с их текущим статусом:</p>
        <ul>
          <li><b>Свободен</b> (зелёный) — готов принять вызов.</li>
          <li><b>В разговоре</b> (синий) — ведёт разговор; отображается длительность текущего звонка.</li>
          <li><b>Вызывает</b> (жёлтый) — входящий вызов, ещё не отвечен.</li>
          <li><b>Недоступен</b> (серый) — оператор не в сети.</li>
        </ul>
        <p>В столбце «Время» — время с последнего события (для свободных и недоступных — время с последнего звонка).</p>
        <p><b>Исторический график</b> — показывает динамику очереди за выбранный период. Переключатель: 1ч / 3ч / 6ч / 12ч / 24ч.</p>
        <ul>
          <li><b>Оранжевая линия</b> — количество ожидающих в очереди.</li>
          <li><b>Синяя линия</b> — количество операторов в разговоре.</li>
          <li><b>Зелёная линия</b> — количество активных (не недоступных) операторов.</li>
          <li><b>Красные треугольники</b> на оси времени — отметки пропущенных звонков в этот момент.</li>
        </ul>
