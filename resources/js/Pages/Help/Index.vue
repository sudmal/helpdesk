        <p><b>Логика видимости территорий</b></p>
        <p>Пользователи видят <b>объединение</b> территорий из двух источников:</p>
        <ul>
          <li><b>Территории бригады</b> — если пользователь входит в бригаду, он автоматически видит территории этой бригады.</li>
          <li><b>Личные территории</b> — назначаются в карточке пользователя (Настройки → Пользователи).</li>
        </ul>
        <p>Пользователь видит заявки и фильтры только по своим территориям (объединение обоих источников). Администраторы и пользователи с полными правами видят все территории без ограничений.</p>
        <p><b>Практика:</b> если технику временно нужно обслуживать чужой участок (подмена коллеги) — достаточно добавить ему эту территорию в карточке пользователя. Менять состав бригады не нужно.</p>
